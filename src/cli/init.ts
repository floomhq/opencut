function writeFile(filePath: string, content: string) {
  fs.writeFileSync(filePath, content, "utf-8");
  console.log(`  created ${filePath}`);
}

function main() {
  const { projectName, template } = parseArgs();
  const projectRoot = path.resolve(__dirname, "../..");
  const examplesDir = path.join(projectRoot, "src/examples");
  const projectDir = path.join(examplesDir, projectName);

  if (fs.existsSync(projectDir)) {
    console.error(`❌ Directory already exists: ${projectDir}`);
    process.exit(1);
  }

  fs.mkdirSync(projectDir, { recursive: true });

  const t = TEMPLATES[template];

  writeFile(path.join(projectDir, "config.ts"), t.config);
  writeFile(path.join(projectDir, "timeline.ts"), t.timeline);
  writeFile(path.join(projectDir, "subtitles.ts"), t.subtitles);
  writeFile(path.join(projectDir, "Root.tsx"), typeof t.root === "function" ? t.root(projectName) : t.root);
  writeFile(path.join(projectDir, "index.ts"), t.index);
  writeFile(path.join(projectDir, "README.md"), typeof t.readme === "function" ? t.readme(projectName) : t.readme);

  console.log(`\n✅ Created "${projectName}" (${template} template) at src/examples/${projectName}/`);
  console.log("\nNext steps:");
  console.log(`  1. Add assets to public/ (facecam video, music, screenshots, etc.)`);
  console.log(`  2. Customize timeline.ts and config.ts`);
  console.log(`  3. Run: npx ts-node src/cli/validate.ts src/examples/${projectName}/timeline.ts`);
  console.log(`  4. Preview:  npx remotion studio src/examples/${projectName}/index.ts`);
  console.log(`  5. Render:   npx remotion render src/examples/${projectName}/index.ts ${kebabToPascal(projectName)} out/${projectName}.mp4`);
}

main();
