const VALID_TEMPLATES = ["facecam", "product", "text-only"] as const;
type Template = (typeof VALID_TEMPLATES)[number];

function parseArgs() {
  const args = process.argv.slice(2);
  const projectName = args[0];
  const templateIdx = args.findIndex((a) => a === "--template");
  const template: Template =
    (templateIdx >= 0 ? (args[templateIdx + 1] as Template) : undefined) ??
    "facecam";

  if (!projectName) {
    console.error("Usage: npx ts-node src/cli/init.ts <project-name> [--template facecam|product|text-only]");
    process.exit(1);
  }

  if (!VALID_TEMPLATES.includes(template)) {
    console.error(`Invalid template "${template}". Valid: ${VALID_TEMPLATES.join(", ")}`);
    process.exit(1);
  }

  return { projectName, template };
}

function kebabToPascal(str: string): string {
  return str
    .split("-")
    .map((s) => s.charAt(0).toUpperCase() + s.slice(1))
    .join("");
}

function kebabToCamel(str: string): string {
  const pascal = kebabToPascal(str);
  return pascal.charAt(0).toLowerCase() + pascal.slice(1);
}

